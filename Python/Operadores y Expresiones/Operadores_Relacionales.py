#Tipo Lógico
print(1 + 1 == 3) 
print(1 + 1 == 2)
#   Operadores Relacionales
#Sirven para comparar dos valores, dependiendo del resultado de la comparación se devolverá:
#Verdadero (True), si es cierta
#Falso (False), si no es cierta

#  1.- Igual que
print(3 == 2)

#  2.- Distinto de 
print(3!=2)
 
#  3.- Mayor que 
print (3>2)

#  4.- Menor que
print (3<2)

#  5.- Mayor o igual que
print (3>=4)

#  6.- Menor o igual que 
print (3<=4) 
